import { execSync } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const repoRoot = execSync("git rev-parse --show-toplevel", { encoding: "utf8" }).trim();

function git(command) {
  return execSync(command, { encoding: "utf8", cwd: repoRoot }).trim();
}

function writeCursorContext() {
  const hash = git("git rev-parse HEAD");
  const shortHash = git("git rev-parse --short HEAD");
  const subject = git("git log -1 --pretty=%s");
  const body = git("git log -1 --pretty=%b");
  const author = git("git log -1 --pretty=%an");
  const committedAt = git("git log -1 --date=iso-strict --pretty=%ad");
  const changedFilesRaw = git("git show --name-only --pretty='' HEAD");
  const changedFiles = changedFilesRaw
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean);

  const contextPath = resolve(repoRoot, ".cursor/context/last-commit.md");
  mkdirSync(dirname(contextPath), { recursive: true });

  const content = `# Latest Commit Context

This file is generated by \`scripts/post-commit-clockwork.mjs\`.
It is repo-local and updates on every commit.

## Commit

- Hash: \`${hash}\`
- Short hash: \`${shortHash}\`
- Subject: ${subject}
- Author: ${author}
- Date: ${committedAt}

## Body

${body || "(no body)"}

## Changed files

${changedFiles.length ? changedFiles.map((file) => `- \`${file}\``).join("\n") : "- (no files listed)"}
`;

  writeFileSync(contextPath, content, "utf8");
}

writeCursorContext();
